#include "libft.h"

int	ft_atoi(const char *nptr)
{
	unsigned int	i;
	long long		res;
	long long		k;

	i = 0;
	k = 1;
	res = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			k = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = res * 10 + (nptr[i] - '0') * k;
		i++;
		if (res > 0 && k < 0)
			return (0);
		if (res < 0 && k > 0)
			return (-1);
	}
	return (res);
}
